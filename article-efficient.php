<h2>Keeping an Efficient Work from Home Day</h2>
<p>Even though working from home seems very different from working in a stereotypical office environment, as a Remote Staff contractor you'll be expected to take full responsibility for all task deliverables required by your international employer.  Learn to manage your time and resources effectively so that you and your client won't have any issues. </p>

<p><strong>Below are a few handy TO-DOs in your performance as a Remote Staff contractor: </strong></p>
  
<p><strong>Start Your Day On Schedule</strong> – If you develop and apply a mindset that you consistently have to be at work on time and do your tasks everyday, you'll feel more professional even though you're based at home. </p>
    
<p><strong>Stick to a Practical Routine</strong> – You need to abide by the daily work schedule which corresponds to your international employer's timezone.  For example, if you work with Australian clients, then you should be working from 6AM to 3PM or 7AM to 4PM, with a one-hour lunch break at 11PM or 12NOON Manila time.  Resist the urge to do non-work related stuff and avoid cramming to finish your task towards the end of the day. </p>
    
<p><strong>Have a 'NO FAMILY/FRIENDS ON WORK HOURS' Policy</strong> – Any employer will normally take issue or frown upon employees who entertain a friend, a loved one or a family member during official work hours, because this distracts focus and disrupts work-flow.  This same principle applies when you're working as a Remote Staff contractor.  To mitigate potential issues with your client, refrain from engaging in personal stuff or having friends/family over during business hours. </p>
    
<p><strong>Designate Your Home-Work Space</strong> – Make sure you have a spot in your home that you can truly call “your private office”, whether it’s in another room, or just a corner.  Inform your household that it is YOUR OFFICE and OFF LIMITS to them during your work hours.</p> 
    
<p><strong>Enlighten Family and Friends on the Merits of Your Work</strong> – Exert effort to educate and inform these people just how business-like this line of work is.  Explain to them that even though you're working from home, it's still a regular, full-time job.  Many folks think that working from home is just part-time or just a hobby.  You need to make them understand that you may be based at home, but your job responsibilities and results expected by your international employers are very much similar (if not greater than) with those working in typical office environments.  Communicate and show to your family/friends that you are a contracted professional and that you treat this job seriously, so they should likewise take it seriously.</p>
    
<p><strong>Find Work-Life Balance</strong> – Being isolated in your home-work space and working long hours in-front of your computer can take a bit of a toll on your stamina and leave you with less energy to spend time with your family and loved ones.  To get out of this rut, try and catch up, dine out or watch a movie with your wife, your kids, your friends after work hours every weekday.  Go out jogging or power-walking or head off to the gym tone up your sore, stiff muscles! </p>
    
<p><strong>Stick to Your Daily Schedule </strong>– Regularly check to see if every hour of your working day is marked down in a calendar and keep it where you can see it often.  This will keep you focused on your action-items and task-deliverables.  Create a TO DO LIST at the start of your day, and tick items that are done and pending by the day's end.</p>



